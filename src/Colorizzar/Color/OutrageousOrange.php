<?php

namespace Colorizzar\Color;

class OutrageousOrange implements HtmlColor
{
    public function getColorName()
    {
        return 'OutrageousOrange';
    }

    public function getHex()
    {
        return '#FF6E4A';
    }

    public function getRgb()
    {
        return (array) [255, 110, 74];
    }
}

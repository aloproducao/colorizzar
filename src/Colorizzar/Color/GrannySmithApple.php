<?php

namespace Colorizzar\Color;

class GrannySmithApple implements HtmlColor
{
    public function getColorName()
    {
        return 'GrannySmithApple';
    }

    public function getHex()
    {
        return '#A8E4A0';
    }

    public function getRgb()
    {
        return (array) [168, 228, 160];
    }
}
